#include "ft_printf.h"

char	*ft_stricpy(char *dst, int *i, const char *src)
{
	int	j;
	int n;

	n = 0;
	j = *i;
	while (src[n] != '\0')
		dst[j++] = src[n++];
	dst[j] = '\0';
	*i = j;
	return (dst);
}
