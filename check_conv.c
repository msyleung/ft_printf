#include "ft_printf.h"

int	check_conv(t_struct *d)
{
	if (d->fo[d->i] == 's' || d->fo[d->i] == 'S' || d->fo[d->i] == 'p' ||
			d->fo[d->i] == 'i' || d->fo[d->i] == 'd' || d->fo[d->i] == 'D' ||
			d->fo[d->i] == 'o' || d->fo[d->i] == 'O' || d->fo[d->i] == 'u' ||
			d->fo[d->i] == 'U' || d->fo[d->i] == 'x' || d->fo[d->i] == 'X' ||
			d->fo[d->i] == 'c' || d->fo[d->i] == 'C' || d->fo[d->i] == '%')
	{
		d->conv = d->fo[d->i];
		return (1);
	}
	return (0);
}
