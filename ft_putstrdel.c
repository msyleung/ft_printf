#include "ft_printf.h"

int	ft_putstrdel(char **as, int ti)
{
	int	n;

	n = 0;
	if (!as)
		return (0);
	if (*as != NULL)
	{
		as[0][ti] = '\0';
		while (n < ti)
			write(1, &(as[0][n++]), 1);
		free(*as);
		*as = NULL;
	}
	return (n);
}
