#include "libftprintf.h"

int	ft_intlen(int nb)
{
	int	c;

	c = 1;
	if (nb < 0)
	{
		c++;
		nb *= -1;
	}
	while (nb >= 10)
	{
		nb = nb / 10;
		c++;
	}
	return (c);
}
